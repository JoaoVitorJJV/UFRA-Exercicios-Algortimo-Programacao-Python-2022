# Faça um algoritmo calcular a área do quadrado, o usuário deve informar o lado do
# quadrado para o calculo

# Entrada: 1° e 2° lado de um quadrado
print("Àrea do quadrado - 1\n")
lado1 = int(input("Informe o 1° lado do quadrado: "))
lado2 = int(input("Informe o 2° lado do quadrado: "))

# Fórmula da Àrea: lado x lado ou lado²
area_quadrado = lado1 * lado2

# Saída
print(f"A Àrea do quadrado é de: {area_quadrado}")